print("hello python".islower())
# output ➜ True

print("Hello Python".islower())
# output ➜ False

print("HELLO PYTHON".islower())
# output ➜ False

print("hello python".istitle())
# output ➜ False

print("Hello Python".istitle())
# output ➜ True

print("HELLO PYTHON".istitle())
# output ➜ False

print("hello python".isupper())
# output ➜ False

print("Hello Python".isupper())
# output ➜ False

print("HELLO PYTHON".isupper())
# output ➜ True